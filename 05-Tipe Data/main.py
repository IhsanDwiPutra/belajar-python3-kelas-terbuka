# Tipe data: angka satuan (integer)
dataInteger = 1
print("data :", dataInteger, ",Tipenya adalah", type(dataInteger))

# Tipe data: angka dengan koma (float)
dataFloat = 1.5
print("data :", dataFloat, ",Tipenya adalah", type(dataFloat))

# Tipe data: kumpulan karakter (string)
dataString = "Agus"
print("data :", dataString, ",Tipenya adalah", type(dataString))

# Tipe data: biner true/false (boolean)
dataBool = True
print("data :", dataBool, ",Tipenya adalah", type(dataBool))

# Tipe data khusus

# bilangan kompleks
dataComplex = complex(5,6)
print("data :", dataComplex, ",Tipenya adalah", type(dataComplex))

# tipe data dari bahasa C
from ctypes import c_double

dataCDouble = c_double(10.5)
print("data :", dataCDouble, ",Tipenya adalah", type(dataCDouble))


















