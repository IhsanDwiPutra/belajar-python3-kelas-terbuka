# Operasi Komparasi
# setiap hasil dari operasi komparasi adalah boolean
# >,<,>=,<=,==,!=,is,is not

a = 5
b = 2

# lebih besar dari >
hasil = a > b
print(a,'>',b,'=',hasil)

# lebih kecil dari <
hasil = a < b
print(a,'<',b,'=',hasil)

# lebih besar sama dengan >=
a = 5
b = 5
hasil = a >= b
print(a,'>=',b,'=',hasil)

# lebih kecil sama dengan <=
hasil = a <= b
print(a,'<=',b,'=',hasil)

# sama dengan =
hasil = a == b
print(a,'==',b,'=',hasil)

# tidak sama dengan !=
hasil = a != b
print(a,'!=',b,'=',hasil)

# menggunakan is
hasil = a is b
print(a,'is',b,'=',hasil)

# menggunakan is not
hasil = a is not b
print(a,'is not',b,'=',hasil )














