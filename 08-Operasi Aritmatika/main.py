# Operasi Aritmatika

a = 10
b = 4

# operasi tambah +
hasil = a + b
print(a,"+",b,"=",hasil)

# operasi kurang -
hasil = a - b
print(a,"-",b,"=",hasil)

# operasi kali x
hasil = a * b
print(a,"x",b,"=",hasil)

# operasi bagi /
hasil = a / b
print(a,"/",b,"=",hasil)

# operasi eksponen (pangkat) **
hasil = a ** b
print(a,"pangkat",b,"=",hasil)

# operasi modulus %
hasil = a % b
print(a,"% (sisa pembagian)",b,"=",hasil)

# operasi floor division //
hasil = a // b
print(a,"//",b,"=",hasil)

