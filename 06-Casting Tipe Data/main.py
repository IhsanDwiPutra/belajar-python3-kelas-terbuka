# Casting Tipe Data
# tipe data = int, float, str, bool

# Integer
dataInteger = 10
print("=== Integer ===")
print("Integer  :", dataInteger, ", Tipe:", type(dataInteger))
print("Di casting ke:")
dataFloat = float(dataInteger)
print("Float    :", dataFloat, ", Tipe:", type(dataFloat))
dataString = str(dataInteger)
print("String   :", dataString,  ", Tipe:", type(dataString))
dataBool = bool(dataInteger)
print("Boolean  :", dataBool, ", Tipe:", type(dataBool))
print("==================")

# Float
dataFloat = 0.1
print("\n=== Float ===")
print("Float    :", dataFloat, ",Tipe:", type(dataFloat))
print("Di casting ke:")
dataInt = int(dataFloat)
print("Integer  :", dataInt,",Tipe:", type(dataInt))
dataStr = str(dataFloat)
print("String   :", dataStr,",Tipe:",type(dataStr))
dataBool = bool(dataFloat)
print("Boolean  :", dataBool,",Tipe:", type(dataBool))
print("==================")

# String
